export type CeoQuote = (typeof CEO_QUOTES)[number];

export function getRandomCeoQuotes(count = 4, random = Math.random): CeoQuote[] {
  const quoteCount = Math.max(0, Math.min(CEO_QUOTES.length, Math.floor(count)));
  const quotes = [...CEO_QUOTES];

  for (let index = quotes.length - 1; index > 0; index -= 1) {
    const swapIndex = Math.floor(random() * (index + 1));
    [quotes[index], quotes[swapIndex]] = [quotes[swapIndex], quotes[index]];
  }

  return quotes.slice(0, quoteCount);
}

